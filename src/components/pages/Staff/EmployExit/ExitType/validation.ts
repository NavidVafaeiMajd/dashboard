import { z } from "zod";

export const validation = z.object({
  name: z
    .string()
    .min(1, "نام نوع انفصال الزامی است")
    .min(2, "نام نوع انفصال باید حداقل 2 کاراکتر باشد")
    .max(50, "نام نوع انفصال نباید بیشتر از 50 کاراکتر باشد")
    .regex(/^[\u0600-\u06FF\s]+$/, "فقط حروف فارسی مجاز است"),
});
